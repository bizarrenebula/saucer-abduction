/* ---- engine ---- */
const _listeners = new Set();
export function getLang(){ return _lang; }
export function onLang(fn){ _listeners.add(fn); return ()=>_listeners.delete(fn); }

export function t(key, params){
  const table = STRINGS[_lang] || STRINGS.en;
  let s = (table[key] != null) ? table[key] : (STRINGS.en[key] != null ? STRINGS.en[key] : key);
  if(params) for(const k in params) s = s.split('{'+k+'}').join(params[k]);
  return s;
}

/* Apply translations to static markup: [data-i18n] -> textContent,
   [data-i18n-html] -> innerHTML. Also mark the active language button(s). */
export function applyStaticDOM(root){
  const r = root || document;
  r.querySelectorAll('[data-i18n]').forEach(el=>{ el.textContent = t(el.getAttribute('data-i18n')); });
  r.querySelectorAll('[data-i18n-html]').forEach(el=>{ el.innerHTML = t(el.getAttribute('data-i18n-html')); });
  r.querySelectorAll('[data-lang]').forEach(el=>{ el.classList.toggle('on', el.getAttribute('data-lang')===_lang); });
}

export function setLang(code){
  if(!LANGS.some(l=>l.code===code) || code===_lang) { // still refresh active markers on same-lang click
    if(code===_lang){ applyStaticDOM(); }
    return;
  }
  _lang = code;
  try { localStorage.setItem(STORE_KEY, code); } catch(e){}
  document.documentElement.setAttribute('lang', code);
  applyStaticDOM();
  _listeners.forEach(fn=>{ try{ fn(code); }catch(e){} });
}

